namespace SMOL_Solution;

public enum Directions {

    /**
     * The player moves up.
     */
    Up,

    /**
     * The player moves down.
     */
    Down,

    /**
     * The player moves left.
     */
    Left,

    /**
     * The player moves right.
     */
    Right,

    /**
     * The player stayes still in the axis x.
     */
    StayX,

    /**
     * The player stayes still in the axis y.
     */
    StayY
}